/* VISTE */
create view vit_es as
select titolo, e.ora_inizio
from canzone as c join esibizione as e on e.canzone=c.codice
join serata as s on s.data_serata=e.serata
join esc on esc.vittoria=c.codice
where s.codice<>3;

create view art_per_canz as
select c.codice as canzone,count(*) as num_artisti
from canzone as c join interpretazione as i on c.codice=i.canzone
group by c.codice;

drop view if exists pnt_3_art;
create view pnt_3_art as
select c.codice,num_artisti, sum(v.punteggio)
from voto as v join serata as s on v.serata=s.data_serata join
canzone as c on c.codice=v.canzone join art_per_canz as apc on apc.canzone=c.codice
where apc.num_artisti>2 and s.codice=3
group by c.codice,num_artisti;

