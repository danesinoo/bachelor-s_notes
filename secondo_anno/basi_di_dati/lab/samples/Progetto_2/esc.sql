/* CREAZIONE TABELLE */

CREATE TABLE Luogo(
	nome varchar(40) not null,
	citta varchar(20) not null,
	nazione varchar(3) not null,
	capienza int not null,
	PRIMARY KEY (nome, citta)
);

CREATE TABLE Canzone (
    codice varchar(5) not null,
    titolo varchar(30) not null,
    durata interval hour to minute not null,
    genere varchar(30) not null,
    partecipazione int not null,
    nazione varchar(3) not null,
    PRIMARY KEY (codice)
);

CREATE TABLE ESC (
  anno int not null,
  nazione_ospitante varchar(3) not null,
  slogan varchar(30) not null,
  luogo varchar(40) not null,
  citta varchar(20) not null,
  vittoria varchar(5),
  PRIMARY KEY  (anno),
  FOREIGN KEY (luogo,citta) REFERENCES Luogo(nome,citta),
  FOREIGN KEY (vittoria) REFERENCES Canzone(codice)
);
ALTER TABLE Canzone
ADD FOREIGN KEY (partecipazione) REFERENCES ESC(anno); 

CREATE TABLE Serata(
	data_serata date not null,
	codice int not null,
	ora_inizio time not null,
	ora_fine time not null,
	esc int not null,
	PRIMARY KEY (data_serata),
	FOREIGN KEY (esc) REFERENCES ESC(anno)
);

CREATE TABLE Nazione_Votante(
	serata date not null,
	nazione varchar(3) not null,
	PRIMARY KEY(serata,nazione),
	FOREIGN KEY (serata) REFERENCES Serata(data_serata)
);

CREATE TABLE Spokeperson(
	codice_fiscale varchar(16) not null,
	cognome varchar(30) not null,
	nome varchar(30) not null,
	nazione_rappresentanza varchar(3) not null,
	PRIMARY KEY(codice_fiscale)
);

CREATE TABLE Annuncio_Voti(
	spokeperson varchar(16) not null,
	serata date not null,
	posizione int not null,
	PRIMARY KEY (spokeperson, serata),
	FOREIGN KEY (spokeperson) REFERENCES Spokeperson(codice_fiscale),
	FOREIGN KEY (serata) REFERENCES Serata(data_serata)
);

CREATE TABLE Esibizione(
	id int not null,
	serata date not null,
	tipo varchar(40) not null,
	ora_inizio time not null,
	ora_fine time not null,
	canzone varchar(5) default null,
	PRIMARY KEY(id),
	FOREIGN KEY (serata) REFERENCES Serata(data_serata),
	FOREIGN KEY (canzone) REFERENCES Canzone(codice)
);

CREATE TABLE Prova_Esibizione(
	tipo varchar(30) not null,
	esibizione int not null,
	data_prova date not null,
	ora_inizio time not null,
	ora_fine time not null,
	PRIMARY KEY(tipo, esibizione),
	FOREIGN KEY (esibizione) REFERENCES Esibizione(id)
);	

CREATE TABLE Conduttore (
	codice_fiscale varchar(16) not null,
	nome_arte varchar(30) default null,
	cognome varchar(30) not null,
	nome varchar(30) not null,
	nazionalita varchar(3) not null,
	PRIMARY KEY(codice_fiscale)
);

CREATE TABLE Conduzione(
	esc int not null,
	conduttore varchar(16) not null,
	PRIMARY KEY(esc,conduttore),
	FOREIGN KEY (esc) REFERENCES ESC(anno),
	FOREIGN KEY (conduttore) REFERENCES Conduttore(codice_fiscale)
);

CREATE TABLE Performance_Conduttore(
	esibizione int not null,
	conduttore varchar(16) not null,
	PRIMARY KEY (esibizione,conduttore),
	FOREIGN KEY (esibizione) REFERENCES Esibizione(id),
	FOREIGN KEY (conduttore) REFERENCES Conduttore(codice_fiscale)
);

CREATE TABLE Ospite (
	codice_fiscale varchar(16) not null,
	nome_arte varchar(30) default null,
	cognome varchar(30) not null,
	nome varchar(30) not null,
	nazionalita varchar(3) not null,
	PRIMARY KEY(codice_fiscale)
);

CREATE TABLE Performance_Ospite (
    esibizione int not null,
    ospite varchar(16) not null,
    PRIMARY KEY (esibizione, ospite),
    FOREIGN KEY (esibizione) REFERENCES Esibizione(id),
    FOREIGN KEY (ospite) REFERENCES Ospite(codice_fiscale)
);

CREATE TABLE Lingua (
    canzone varchar(5) not null,
    nome varchar(30) not null,
    PRIMARY KEY (canzone, nome),
    FOREIGN KEY (canzone) REFERENCES Canzone(codice)
);

CREATE TABLE Artista (
	codice_fiscale varchar(16) not null,
	nome_arte varchar(30) default null,
	cognome varchar(30) not null,
	nome varchar(30) not null,
	nazionalita varchar(3) not null,
	PRIMARY KEY(codice_fiscale)
);

CREATE TABLE Interpretazione (
    canzone varchar(5) not null,
    artista varchar(16) not null,
    nome_gruppo varchar(30),
    PRIMARY KEY (canzone, artista),
    FOREIGN KEY (canzone) REFERENCES Canzone(codice),
    FOREIGN KEY (artista) REFERENCES Artista(codice_fiscale)
);

CREATE TABLE Selezione_Nazionale (
    anno int not null,
    nazione varchar(3) not null,
    modalit√† varchar(30) not null,
    emittente varchar(30) not null,
    PRIMARY KEY (anno, nazione),
    FOREIGN KEY (anno) REFERENCES ESC(anno)
);

CREATE TABLE Selezione (
    artista varchar(16) not null,
    anno int not null,
    nazione varchar(3) not null,
    PRIMARY KEY (artista, anno, nazione),
    FOREIGN KEY (artista) REFERENCES Artista(codice_fiscale),
    FOREIGN KEY (anno, nazione) REFERENCES Selezione_Nazionale(anno, nazione)
);

CREATE TABLE Votante (
    id varchar(6) not null,
	nazione varchar(3) not null,
    tipo varchar(30) not null,
	anno int not null,
    persone_televotanti int default null,
    PRIMARY KEY (id)
);

CREATE TABLE Voto (
    votante varchar(6),
    canzone varchar(5) not null,
    serata date not null,
    punteggio int not null,
    PRIMARY KEY (votante, canzone, serata),
    FOREIGN KEY (votante) REFERENCES Votante(id),
    FOREIGN KEY (canzone) REFERENCES Canzone(codice),
    FOREIGN KEY (serata) REFERENCES Serata(data_serata)
);

CREATE TABLE Membro_Giuria (
    codice_fiscale varchar(16) not null,
    cognome varchar(30) not null,
    nome varchar(30) not null,
    PRIMARY KEY (codice_fiscale)
);

CREATE TABLE Composizione_Giuria(
	giudice varchar(16) not null,
	giuria varchar(6) not null,
	PRIMARY KEY (giudice,giuria),
	FOREIGN KEY (giudice) REFERENCES Membro_Giuria(codice_fiscale),
	FOREIGN KEY (giuria) REFERENCES Votante(id)
);

CREATE TABLE Collaboratore (
	codice_fiscale varchar(16) not null,
	nome_arte varchar(30) default null,
	cognome varchar(30) not null,
	nome varchar(30) not null,
	nazionalita varchar(3) not null,
	ruolo varchar(30) not null,
	PRIMARY KEY(codice_fiscale)
);

CREATE TABLE Collaborazione (
    collaboratore varchar(16) not null,
    esibizione int not null,
    PRIMARY KEY (collaboratore, esibizione),
    FOREIGN KEY (collaboratore) REFERENCES Collaboratore(codice_fiscale),
    FOREIGN KEY (esibizione) REFERENCES Esibizione(id)
);

