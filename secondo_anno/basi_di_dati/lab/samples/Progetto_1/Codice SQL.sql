create table Cliente (
    Mail        varchar (100)   primary key,
    Nome        varchar (50)    not null,
    Cognome     varchar (50)    not null,
    Telefono    varchar (20)    not null,
    Premium     bool            not null,
    Password    varchar (30)    not null
);

create table Indirizzo (
    ID      int             primary key,
    Via     varchar (50)    not null,
    NCivico varchar (10)    not null,
    CAP     char (5)        not null,
    Citta   varchar (50)    not null,
    Stato   varchar (20)    not null,
    Cliente varchar (100)   not null,
    foreign key (Cliente)   references Cliente(Mail) on update cascade on delete cascade
);

create table Allergene (
    Nome varchar (50) primary key
);

create table Allergie_clienti (
    Allergene   varchar (50),
    Cliente     varchar (100),
    primary key (Allergene, Cliente),
    foreign key (Allergene) references Allergene(Nome)  on update cascade on delete cascade,
    foreign key (Cliente)   references Cliente(Mail)    on update cascade on delete cascade
);

create table Carta_di_credito (
    Numero          char (16)       primary key,
    Circuito        varchar (20)    not null,
    Scadenza        date            not null,
    Intestatario    varchar (50)    not null
);

create table Metodi_pagamento (
    Cliente         varchar (100),
    CartaDiCredito  char (16),
    primary key (Cliente, CartaDiCredito),
    foreign key (Cliente)           references Cliente(Mail)            on update cascade on delete cascade,
    foreign key (CartaDiCredito)    references Carta_di_credito(Numero) on update cascade on delete cascade
);

create table Ristorante (
    ID                  int             primary key,
    Nome                varchar (50)    not null,
    Affiliato           bool            not null,
    PercentualeSconto   int             ,
    Mail                varchar (100)   not null,
    Telefono            varchar (20)    not null,
    CAP                 char (5)        not null,
    Via                 varchar (50)    not null,
    NCivico             varchar (10)    not null,
    CittÃ                varchar (50)    not null,
    Stato               varchar (20)    not null,
    PunteggioMedio      decimal (2,1)   ,
    check (PercentualeSconto > 5 and PercentualeSconto < 100)
);

create table Piatto (
    Nome        varchar (50)    ,
    Ristorante  int             ,
    Prezzo      decimal (7,2)   not null,
    primary key (Nome, Ristorante),
    foreign key (Ristorante)    references Ristorante(ID) on update cascade on delete cascade
);

create table Allergie_piatti (
    Piatto      varchar (50),
    Ristorante  int,
    Allergene   varchar (50),
    primary key (Piatto, Ristorante, Allergene),
    foreign key (Piatto, Ristorante)    references Piatto(Nome, Ristorante) on update cascade on delete cascade,
    foreign key (Allergene)             references Allergene(Nome)          on update cascade on delete cascade
);

create table Giorno (
    Ristorante  int     ,
    Data        date    ,
    OraApertura time    ,
    OraChiusura time    not null,
    primary key (Ristorante, Data, OraApertura),
    foreign key (Ristorante) references Ristorante(ID) on update cascade on delete cascade
);

create table Rider (
    CF          char (16)       primary key,
    Nome        varchar (50)    not null,
    Cognome     varchar (50)    not null,
    Mail        varchar (100)   not null,
    Telefono    varchar (20)    not null,
    Dipendente  bool            not null,
    IBAN        char (27)       not null,
    Stipendio   decimal (7,2)   ,
    Ristorante  int             ,
    foreign key (Ristorante)    references Ristorante(ID) on update cascade on delete set null
);

create table Ordine (
    ID                  int             primary key,
    DataoraPagamento    timestamp       not null,
    DataoraRitiro       timestamp       ,
    Spedizione          bool            not null,
    Cliente             varchar (100)   ,
    CartaDiCredito      char (16)       ,
    Ristorante          int             ,
    foreign key (Cliente)           references Cliente(Mail)            on update cascade on delete set null,
    foreign key (CartaDiCredito)    references Carta_di_credito(Numero) on update set null on delete set null,
    foreign key (Ristorante)        references Ristorante(ID)           on update cascade on delete set null,
    check (
        (Spedizione = true and DataoraRitiro is null) or (
            Spedizione = false and
            DataoraRitiro is not null and
            DataoraPagamento <= DataoraRitiro
        )
    )
);

create table Piatti_ordinati (
    Piatto      varchar (50),
    Ristorante  int         ,
    Ordine      int         ,
    Quantita    int         not null,
    primary key (Piatto, Ristorante, Ordine),
    foreign key (Piatto, Ristorante)    references Piatto(Nome, Ristorante) on update cascade on delete cascade,
    foreign key (Ordine)                references Ordine(ID)               on update cascade on delete cascade
);

create table Spedizione (
    Ordine          int             primary key,
    DataoraMax      timestamp       not null,
    DataoraMin      timestamp       not null,
    DataoraArrivo   timestamp       not null,
    CostoSpedizione decimal (5,2)   not null,
    Indirizzo       int             ,
    Rider           char (16)       ,
    foreign key (Ordine)    references Ordine(ID)       on update cascade on delete cascade,
    foreign key (Indirizzo) references Indirizzo(ID)    on update cascade on delete set null,
    foreign key (Rider)     references Rider(CF)        on update cascade on delete set null,
    check (DataoraMin <= DataoraMax)
);

create table Recensione (
    Ordine              int             primary key,
    Testo               varchar (500)   ,
    Ristorante          int             not null,
    Rider               char (16)       ,
    StelleRistorante    int             not null,
    StelleRider         int             ,
    foreign key (Ordine)        references Ordine(ID)       on update cascade on delete cascade,
    foreign key (Ristorante)    references Ristorante(ID)   on update cascade on delete cascade,
    foreign key (Rider)         references Rider(CF)        on update cascade on delete set null,
    check (
        StelleRistorante <= 5 and StelleRistorante >= 0 and
        StelleRider <= 5 and StelleRider >= 0
    )
);

